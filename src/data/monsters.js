const counters = ['Квен и уклонения', 'Ирден и точный тайминг', 'Игни и алхимическая подготовка', 'Аард и контроль дистанции']

const hash = (value) => {
    let output = 0
    for (let i = 0; i < value.length; i += 1) {
        output = (output * 31 + value.charCodeAt(i)) >>> 0
    }
    return output
}

const makeDescription = (name, group) => {
    const seed = hash(`${name}:${group}`)
    const counter = counters[seed % counters.length]
    const trait = traits[group] || 'опасным боевым стилем'
    const habitat = habitats[group] || 'в диких и малоизученных местах'
    const variant = seed % 3

    if (variant === 0) {
        return `${name} известен ${trait}. Чаще всего его встречают ${habitat}, и в бою он быстро ломает темп схватки. Ведьмаку лучше заходить на охоту с расчетом на ${counter}.`
    }
    if (variant === 1) {
        return `Охота на ${name} требует подготовки: противник выделяется ${trait} и особенно опасен ${habitat}. Ошибки в позиционировании он наказывает мгновенно, поэтому надежнее всего работают ${counter}.`
    }
    return `${name} относится к категории «${group}» и сочетает ${trait}. Обычно активен ${habitat}, где чувствует себя максимально уверенно. В сражении помогает выдержанный ритм и ${counter}.`
}

